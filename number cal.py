bir_son = float(input('birinchi sonni kiriting:'))
ikki_son = float(input("ikkinchi sonni kiriting:"))

qush = bir_son+ikki_son
ayir = bir_son-ikki_son
kup = bir_son*ikki_son
bul = bir_son/ikki_son
print(f" {bir_son} + {ikki_son} = {qush} \n {bir_son}-{ikki_son}= {ayir}"
      f"\n {bir_son}*{ikki_son} = {kup} \n {bir_son}/{ikki_son} {bul}")